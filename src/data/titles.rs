pub fn aliases_for(title: &str) -> Vec<String> {
    ALIAS_MAP
        .get(title)
        .map(|aliases| aliases.iter().map(|item| (*item).to_owned()).collect())
        .unwrap_or_else(|| vec![title.to_owned()])
}

pub fn common_aliases_for(title: &str) -> Vec<String> {
    aliases_for(title)
        .into_iter()
        .filter(|alias| !is_dialect_alias(alias))
        .collect()
}

pub fn dialect_aliases_for(title: &str) -> Vec<String> {
    aliases_for(title)
        .into_iter()
        .filter(|alias| is_dialect_alias(alias))
        .collect()
}

